#include <unistd.h>

void	ft_putnbr(int nb)
{
	long	n;
	int		i;
	int		tmp;

	n = (long)nb;
	if (nb < 0)
	{
		n = -n;
		write(1, "-", 1);
	}
	i = 1;
	while (n / i >= 10)
		i *= 10;
	while (i > 0)
	{
		tmp = n / i % 10 + 48;
		write(1, &tmp, 1);
		i /= 10;
	}
}
