int	ft_atoi(char *str)
{
	int nb;
	int min;

	min = 0;
	nb = 0;
	while (*str && (*str == ' ' || (*str >= '\t' && *str <= '\r')))
		++str;
	while (*str && (*str == '-' || *str == '+'))
		if (*str++ == '-')
			++min;
	while (*str && (*str >= '0' && *str <= '9'))
		nb = (nb * 10) + (*str++ - 48);
	return ((min % 2 == 0) ? nb : -nb);
}
