int	ft_atoi(const char *str)
{
	int n;
	int min;

	min = 1;
	while (*str && (*str == ' ' || (*str >= '\t' && *str <= '\r')))
		++str;
	if (*str == '+' || *str == '-')
		if (*str++ == '-')
			min = -1;
	n = 0;
	while (*str && *str >= '0' && *str <= '9')
		n = n * 10 + *str++ - 48;
	return (n * min);
}
