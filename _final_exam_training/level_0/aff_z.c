#include <unistd.h>

int	main(int ac, char **av)
{
	int i;

	if (ac == 2)
	{
		i = -1;
		while (av[1][++i])
		{
			if (av[1][i] == 'z')
			{
				write(1, &av[1][i], 1);
				write(1, "\n", 1);
				return (0);
			}
		}
		write(1, "z\n", 2);
	}
	else
		write(1, "z\n", 2);
	return (0);
}
