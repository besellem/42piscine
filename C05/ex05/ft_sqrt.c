int	ft_sqrt(int nb)
{
	int f;

	if (nb <= 0)
		return (0);
	f = 0;
	while (f <= 46341 && (f * f) <= nb)
	{
		if ((f * f) == nb)
			return (f);
		++f;
	}
	return (0);
}
